var fs = require("fs");
function writeFileContent(content) {
	var tag = fs.existsSync("./log.txt");
	if(tag) {
		var result = fs.readFileSync("./log.txt");
		fs.writeFileSync("./log.txt", result + "\r\n" + content);
	}
	else {
		fs.writeFileSync("./log.txt", content);
	}
}
